/***

Implement a countdown timer that counts down from 21 to 15.

 */



// for loop
for(let count = 21; count >= 15; count--){
    console.log(count);
}
console.log("-----------1");

// while loop 
let count2 = 21;
while(count2 >= 15){
    console.log(count2);
    count2--
}


console.log("-----------2");
//  do while loop 

let count3 = 21;
do{
console.log(count3);
    count3--
}
while(count3 >= 15);

console.log("-----------3");
